export const PUZZLE_LEVELS: PuzzleLevel[] = [
  ...LEVELS_1_70,
  ...LEVELS_71_140,
  ...LEVELS_141_200,
]

// ── Helper functions ─────────────────────────────────────────────────────────
export function getPuzzle(id: number): PuzzleLevel {
  return PUZZLE_LEVELS.find(p => p.id === id) || PUZZLE_LEVELS[0]
}

export function getLevelsByDifficulty(difficulty: string): PuzzleLevel[] {
  return PUZZLE_LEVELS.filter(p => p.difficulty === difficulty)
}

export function getMaxAttempts(size: number): number {
  return size * size * 3
}

export function getRandomReactionPuzzle(difficulty: string): PuzzleLevel {
  const pool = getLevelsByDifficulty(difficulty)
  return pool[Math.floor(Math.random() * pool.length)]
}
