// ─── Helper functions ─────────────────────────────────────────────────────────

export function computeClues(solution: number[][]): NonogramClues {
  const size = solution.length

  const rowClues: number[][] = solution.map(row => {
    const clues: number[] = []
    let count = 0
    for (const cell of row) {
      if (cell === 1) {
        count++
      } else if (count > 0) {
        clues.push(count)
        count = 0
      }
    }
    if (count > 0) clues.push(count)
    return clues.length > 0 ? clues : [0]
  })

  const colClues: number[][] = []
  for (let c = 0; c < size; c++) {
    const clues: number[] = []
    let count = 0
    for (let r = 0; r < size; r++) {
      if (solution[r][c] === 1) {
        count++
      } else if (count > 0) {
        clues.push(count)
        count = 0
      }
    }
    if (count > 0) clues.push(count)
    colClues.push(clues.length > 0 ? clues : [0])
  }

  return { rowClues, colClues }
}

export function checkSolution(
  playerGrid: CellState[][],
  solution: number[][],
): boolean {
  const size = solution.length
  for (let r = 0; r < size; r++) {
    for (let c = 0; c < size; c++) {
      const playerFilled = playerGrid[r][c] === 'filled'
      const shouldFill   = solution[r][c] === 1
      if (playerFilled !== shouldFill) return false
    }
  }
  return true
}

export function getRowCompletion(
  playerGrid: CellState[][],
  solution: number[][],
  row: number,
): boolean {
  const size = solution[0].length
  for (let c = 0; c < size; c++) {
    const playerFilled = playerGrid[row][c] === 'filled'
    const shouldFill   = solution[row][c] === 1
    if (playerFilled !== shouldFill) return false
  }
  return true
}

export function getPuzzlesByDifficulty(difficulty: string): NonogramPuzzle[] {
  return ALL_NONOGRAM_PUZZLES.filter(p => p.difficulty === difficulty)
}

export function getPuzzle(id: number): NonogramPuzzle | undefined {
  return ALL_NONOGRAM_PUZZLES.find(p => p.id === id)
}
