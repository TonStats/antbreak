export function getRiddles(count: number, difficulty: RiddleDifficulty): Riddle[] {
  return RIDDLES
    .filter(r => r.difficulty === difficulty)
    .sort(() => Math.random() - 0.5)
    .slice(0, count)
}

export function getDailyRiddle(): Riddle {
  const day  = new Date().getDate() - 1
  const seed = day % RIDDLES.length
  return RIDDLES[seed]
}

export function checkAnswer(userInput: string, riddle: Riddle): boolean {
  const clean = (s: string) =>
    s.toLowerCase()
     .trim()
     .replace(/^(a |an |the )/, '')
     .replace(/[^a-z0-9]/g, '')

  const cleaned   = clean(userInput)
  const correct   = clean(riddle.answer)
  const alternates = riddle.alternateAnswers.map(clean)

  return cleaned === correct || alternates.includes(cleaned)
}
